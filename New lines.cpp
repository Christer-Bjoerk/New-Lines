#include <iostream>

using namespace std;

int main() 
{
	// Create a new line with \n character
	cout << "Hello World \n";

	// Two \n creates a blank space
	cout << "Hello World \n\n";
	cout << "I'm learning C++ \n\n";

	// endl is an alternative to \n
	cout << "Hello World" << endl;
	cout << "I'm learning C++";

	return 0;
}