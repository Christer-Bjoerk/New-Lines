#include <iostream>

using namespace std;

int main() 
{
	// Create a new line with \n character
	cout << "Hello World \n";

	// Two \n creates a blank space
	cout << "Hello World \n\n";
	cout << "I'm learning C++ \n\n";

	// endl is an alternative to \n
	cout << "Hello World" << endl;
	cout << "I'm learning C++\n" << endl;

	// Horizontal tab sequence
	cout << "This is me using\t the horizontal tab\n" << endl;

	// Backslash character
	cout << "This is how to create a backlash character\\ \n\n";

	// Writing a Quote
	cout << "\"Quote\".";
	return 0;
}